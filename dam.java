public class dam {
    
    static int add(int a, int b){
        int result=a+b;
        return result;
    }
    static int sub(int a, int b){
        int result=a-b;
        return result;
    }

    public static void main(String []args){
        int a=10;
        int b=20;
        System.out.println(add(a,b));
        System.out.println(sub(a,b));
    }
    
}
